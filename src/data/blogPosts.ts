// Fungsi helper
export function getAllPosts() {
  return blogPosts.sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
}

export function getPostBySlug(slug: string) {
  return blogPosts.find(post => post.slug === slug);
}

export function getPostsByCategory(category: string) {
  if (category === 'all') return getAllPosts();
  return blogPosts.filter(post => post.category.toLowerCase().replace(' ', '-') === category);
}

export function getRecentPosts(limit: number = 3) {
  return getAllPosts().slice(0, limit);
}

export function getPopularTags() {
  const tagCount: Record<string, number> = {};
  blogPosts.forEach(post => {
    post.tags.forEach(tag => {
      tagCount[tag] = (tagCount[tag] || 0) + 1;
    });
  });
  
  return Object.entries(tagCount)
    .sort(([,a], [,b]) => b - a)
    .slice(0, 10)
    .map(([name, count]) => ({ name, count }));
}

export function getBlogStats() {
  const totalArticles = blogPosts.length;
  const totalViews = blogPosts.reduce((sum, post) => sum + post.views, 0);
  const categories = [...new Set(blogPosts.map(post => post.category))].length;
  const thisMonth = blogPosts.filter(post => {
    const postDate = new Date(post.date);
    const now = new Date();
    return postDate.getMonth() === now.getMonth() && postDate.getFullYear() === now.getFullYear();
  }).length;
  
  return {
    totalArticles,
    totalViews: `${(totalViews / 1000).toFixed(1)}K`,
    thisMonth,
    categories
  };
}
